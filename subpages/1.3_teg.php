            <h1>91028 - Investigate relationships <br>between tables, equations, graphs</h1>
            
            <br>

            <h3>Summary</h3>
            <p>
                This external standard assesses students' ability to explore connections between three mathematical representations: 
                tables, equations, and graphs. Students develop skills in translating between these forms, like interpreting data 
                in a table to write an equation or sketching a graph that matches a given equation. This standard emphasises
                understanding these relationships to analyse and solve problems.
            </p>

