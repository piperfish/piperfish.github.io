            <h1>91031 - Apply geometric <br>reasoning in solving problems</h1>
            
            <br>

            <h3>Summary</h3>
            <p>
                This external standard is centered around using geometric concepts to solve practical problems. 
                Students would demonstrate their knowledge of shapes, angles, and spatial relationships. 
                This involved analysing situations, selecting appropriate geometric principles, and applying them to find solutions. 
                The problems might involve calculating areas and perimeters, recognising geometric shapes in real-world contexts, or solving spatial reasoning challenges.
