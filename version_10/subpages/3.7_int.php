            <h1>91579 - Apply integration <br>methods in solving problems</h1>
            
            <br>

            <h3>Summary</h3>

            <p>
                This external standard equips students with applying integration methods to solve problems. 
                They'll learn anti-differentiation (finding the integral) and use it to calculate areas 
                under curves and solve other problems involving accumulation. The focus lies on applying 
                these techniques in various contexts to find solutions.
